package com.example.Instructorapi.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;
import org.springframework.data.mongodb.core.index.Indexed;

@Document(collection = "users")
public class User {

    @Id
    private String id;

    private String name;

    @Indexed(unique = true)
    private String email;

    private String password;

    private String role; // Ini dah betul String

    // --- 1. CONSTRUCTOR KOSONG ---
    public User() {}

    // --- 2. CONSTRUCTOR BERPARAMETER ---
    // Pastikan parameter terakhir adalah String role, bukan Role role
    public User(String name, String email, String password, String role) {
        this.name = name;
        this.email = email;
        this.password = password;
        this.role = role;
    }

    // --- 3. GETTERS AND SETTERS ---

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    // Return type mestilah String
    public String getRole() {
        return role;
    }

    // Parameter mestilah String
    public void setRole(String role) {
        this.role = role;
    }
}